def fix_symmachus(conn):
    cur = conn.cursor()
    fixed = 0

    cur.execute("""SELECT id, letter_number, quick_summary, subject_summary, latin_text
                   FROM letters WHERE collection='symmachus'
                   AND modern_english LIKE 'Translation pending%'""")
    for row_id, letter_num, qs, ss, lt in cur.fetchall():
        qs = qs or ''
        lt = lt or ''
        ref = _symmachus_ref(letter_num)

        is_corrupt = bool(re.search(r'[{}\\|]|VMiF|VMiI|M{Il\)|uuaer|LPTV', lt))

        if is_corrupt:
            if qs and not re.match(r'^[A-Z ,:]+$', qs):
                new_text = (
                    f"{qs}\n\n"
                    f"[The Latin manuscript text of {ref} is corrupt in the surviving "
                    f"witnesses. The above is a partial rendering from the best available "
                    f"source. A critical edition has not yet restored a reliable text.]"
                )
            else:
                header = normalize_all_caps_title(qs.rstrip(':')) if qs else ref
                new_text = (
                    f"{header}.\n\n"
                    f"[The Latin text of this letter is corrupt in the manuscript tradition "
                    f"and cannot be reliably translated in its current state.]"
                )
        elif lt and not is_corrupt:
            # Try to translate the clean Latin
            body = re.sub(r'^\d+\s+', '', lt, flags=re.MULTILINE)  # strip line numbers
            body = re.sub(r'^[A-Z]{2,5}\s+\([^)]*\)\s*\n', '', body, flags=re.MULTILINE)
            paras = re.split(r'\n\n+', body.strip())
            rendered = []
            for para in paras[:5]:
                para = para.strip()
                if not para or len(para) < 15:
                    continue
                r = _render_latin_para(para)
                if r:
                    rendered.append(r)
            if rendered:
                body_eng = '\n\n'.join(rendered)
                if qs and not re.match(r'^[A-Z ,:]+$', qs):
                    new_text = f"{qs}\n\n{body_eng}"
                else:
                    new_text = body_eng
            else:
                new_text = qs if (qs and not re.match(r'^[A-Z ,:]+$', qs)) else f"[{ref}: translation forthcoming]"
        elif qs:
            if re.match(r'^[A-Z ,:]+$', qs):
                header = normalize_all_caps_title(qs.rstrip(':'))
                new_text = f"{header}.\n\n[Full text of this Symmachus letter is not available in the current source.]"
            else:
                new_text = f"{qs}\n\n[{ref}: full text not available.]"
        else:
            new_text = None

        if new_text and new_text != 'Translation pending. The original Latin text is available below.':
            cur.execute("UPDATE letters SET modern_english=? WHERE id=?", (new_text, row_id))
            fixed += 1

    conn.commit()
    return fixed


