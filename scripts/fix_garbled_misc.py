def fix_venantius_fortunatus(conn):
    """
    Phase 1: Strip 'I. Latin title\\n' from non-pending letters (~79).
    Phase 2: Handle the 1 pending letter.
    """
    cur = conn.cursor()
    fixed = 0

    # Phase 1: Strip Roman numeral + Latin title first lines
    cur.execute("""SELECT id, modern_english FROM letters
                   WHERE collection='venantius_fortunatus'
                   AND modern_english NOT LIKE 'Translation pending%'""")
    for row_id, text in cur.fetchall():
        if re.match(r'^[IVXLC]+\.\s+[A-Za-z]', text):
            new_text = re.sub(r'^[IVXLC]+\.\s+[^\n]+\n', '', text, count=1)
            if new_text != text:
                cur.execute("UPDATE letters SET modern_english=? WHERE id=?", (new_text, row_id))
                fixed += 1

    # Phase 2: The one pending letter
    cur.execute("""SELECT id, letter_number, quick_summary, subject_summary, latin_text
                   FROM letters WHERE collection='venantius_fortunatus'
                   AND modern_english LIKE 'Translation pending%'""")
    for row_id, letter_num, qs, ss, lt in cur.fetchall():
        qs = qs or ''
        lt = lt or ''

        if qs:
            # Translate Latin subject line if needed
            subject = qs
            # Common patterns in Venantius
            subject = re.sub(r'\bAd\b', 'To', subject)
            subject = re.sub(r'\bItem\b', 'Another letter', subject)
            subject = re.sub(r'\bepisco?p\w+\b', 'bishop', subject)
            subject = re.sub(r'\bcler\w+\b', 'clergy', subject)
            subject = re.sub(r'\bex nomine suo\b', 'in his own name', subject)

            if lt:
                # Show the Latin poem opening in brackets
                body_preview = re.sub(r'^[IVXLC]+\n[^\n]+\n', '', lt, count=1).strip()
                new_text = (
                    f"{subject}\n\n"
                    f"[{body_preview[:300]}]\n\n"
                    f"[This is a verse letter (carmen) by Venantius Fortunatus, "
                    f"Letter {letter_num}. Full translation forthcoming.]"
                )
            else:
                new_text = f"{subject}\n\n[Translation forthcoming.]"
        else:
            new_text = f"[Venantius Fortunatus, Letter {letter_num}]\n\n[Translation forthcoming.]"

        cur.execute("UPDATE letters SET modern_english=? WHERE id=?", (new_text, row_id))
        fixed += 1

    conn.commit()
    return fixed


