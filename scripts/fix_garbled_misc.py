def translate_ennodius_header(latin_text: str) -> str:
    """
    Convert "I. ENNODIVS IOHANNI." → "Ennodius to Johannes."
    """
    m = re.match(r'^[IVXLC]+\.\s+([A-Z]+)\s+([A-Z]+)\.', latin_text)
    if m:
        sender = _latinize_name(m.group(1))
        recipient = _latinize_name(m.group(2))
        return f"{sender} to {recipient}."
    return ''


def _translate_sidonius_greeting(greeting: str) -> str:
    """
    "Sidonius Firmino suo salutem" → "Sidonius to his dear Firminus, greetings."
    """
    m = re.match(r'Sidonius\s+(domino\s+papae\s+)?(\w+)\s+(suo\s+)?salutem', greeting, re.I)
    if m:
        has_title = bool(m.group(1))
        name = m.group(2).capitalize()
        if has_title:
            return f"Sidonius to the Lord Bishop {name}, greetings."
        return f"Sidonius to his dear {name}, greetings."

    m = re.match(r'Claudianus\s+Sidonio\s+papae\s+salutem', greeting, re.I)
    if m:
        return "Claudianus to the Lord Pope Sidonius, greetings."

    return greeting.strip()


def _translate_sidonius_pending(letter_num, qs, lt):
    """Build English text for a pending Sidonius letter."""
    qs = qs or ''
    lt = lt or ''

    # Check pre-written
    ln = int(letter_num) if str(letter_num).isdigit() else letter_num
    if ln in SIDONIUS_PREWRITTEN:
        return SIDONIUS_PREWRITTEN[ln]

    # Parse greeting
    greeting_eng = ''
    if lt:
        hm = re.match(r'^EPISTULA\s+[IVXLC]+\s*\n\n([^\n]+)', lt)
        if hm:
            greeting_eng = _translate_sidonius_greeting(hm.group(1))

    # Normalize ALL-CAPS quick_summary
    if qs.isupper() or re.match(r'^[A-Z ,]+$', qs.rstrip('.')):
        qs_norm = normalize_all_caps_title(qs.rstrip(',.:'))
        if qs_norm.endswith('Greetings'):
            qs_norm = qs_norm[:-len('Greetings')].rstrip(', ') + ', greetings.'
        elif not qs_norm.endswith('.'):
            qs_norm += '.'
        header = greeting_eng or qs_norm
    elif qs:
        header = greeting_eng or qs
    else:
        header = greeting_eng or ''

    if not header:
        return None

    # Build body from Latin if available
    body_parts = []
    if lt:
        # Remove "EPISTULA X\n\n" and greeting line
        body = re.sub(r'^EPISTULA\s+[IVXLC]+\s*\n\n[^\n]+\n\n', '', lt, count=1)
        # Take up to 3 readable Latin paragraphs with partial translation
        paras = re.split(r'\n\n+|\n(?=\d\.)', body.strip())
        for para in paras[:3]:
            para = re.sub(r'^\d+\.\s*', '', para.strip())
            if not para or len(para) < 20:
                continue
            # Keep Latin in bracket notation for transparency
            body_parts.append(f"[{para[:200]}]" if len(para) > 200 else f"[{para}]")

    if not body_parts and qs and not qs.isupper():
        body_parts = [qs]

    if body_parts:
        return header + '\n\n' + '\n\n'.join(body_parts)
    return header + '\n\n[Full translation forthcoming.]'


