def fix_cassiodorus(conn):
    cur = conn.cursor()
    fixed = 0

    # Phase 1a: Strip "VARIAE, BOOK X, LETTER X\n\n" headers
    cur.execute("""SELECT id, modern_english FROM letters
                   WHERE collection='cassiodorus'
                   AND modern_english LIKE 'VARIAE, BOOK%'""")
    for row_id, text in cur.fetchall():
        new_text = re.sub(r'^VARIAE,\s+BOOK\s+\d+,\s+LETTER\s+\d+\s*\n+', '', text, count=1)
        if new_text != text:
            cur.execute("UPDATE letters SET modern_english=? WHERE id=?", (new_text, row_id))
            fixed += 1

    # Phase 1b: Normalize ALL-CAPS first lines in non-pending, non-From: letters
    cur.execute("""SELECT id, modern_english FROM letters
                   WHERE collection='cassiodorus'
                   AND modern_english NOT LIKE 'Translation pending%'
                   AND modern_english NOT LIKE 'VARIAE%'
                   AND modern_english NOT LIKE 'From:%'
                   AND modern_english NOT LIKE 'King %'
                   AND modern_english NOT LIKE 'Pope %'
                   AND modern_english NOT LIKE 'Senator%'
                   AND modern_english NOT LIKE 'Preface%'
                   AND modern_english NOT LIKE 'Book %'
                   AND modern_english NOT LIKE '[NOTE%'""")
    for row_id, text in cur.fetchall():
        first_line = text.split('\n')[0]
        # Must be entirely uppercase (with spaces and punctuation)
        if re.match(r'^[A-Z][A-Z ,.\-]+$', first_line) and len(first_line) > 10:
            normalized = normalize_all_caps_title(first_line)
            new_text = normalized + text[len(first_line):]
            if new_text != text:
                cur.execute("UPDATE letters SET modern_english=? WHERE id=?", (new_text, row_id))
                fixed += 1

    # Phase 2a: Re-fix letters that got old bad fallbacks
    # - [Textual note:...] = old _translate_latin_paragraph fallback
    # - FORMULA (DE COMITE)... = ALL-CAPS Latin not normalized
    cur.execute("""SELECT id, letter_number, quick_summary, subject_summary, latin_text
                   FROM letters WHERE collection='cassiodorus'
                   AND (modern_english LIKE '%[Textual note%'
                        OR modern_english GLOB 'FORMULA*'
                        OR modern_english GLOB '[A-Z][A-Z][A-Z][A-Z][A-Z][A-Z][A-Z]*'
                       )""")
    for row_id, letter_num, qs, ss, lt in cur.fetchall():
        qs = qs or ''
        lt = lt or ''
        ln = int(letter_num) if str(letter_num).isdigit() else letter_num

        if ln in CASSIODORUS_PREWRITTEN:
            new_text = CASSIODORUS_PREWRITTEN[ln]
        elif ln in CASSIODORUS_APPENDIX:
            new_text = CASSIODORUS_APPENDIX[ln]
        else:
            new_text = _cassiodorus_generic(ln, qs, lt)

        if new_text:
            cur.execute("UPDATE letters SET modern_english=? WHERE id=?", (new_text, row_id))
            fixed += 1

    # Phase 2b: Translation pending letters
    cur.execute("""SELECT id, letter_number, quick_summary, subject_summary, latin_text
                   FROM letters WHERE collection='cassiodorus'
                   AND modern_english LIKE 'Translation pending%'""")
    for row_id, letter_num, qs, ss, lt in cur.fetchall():
        qs = qs or ''
        lt = lt or ''
        ln = int(letter_num) if str(letter_num).isdigit() else letter_num

        # Use pre-written translation if available
        if ln in CASSIODORUS_PREWRITTEN:
            new_text = CASSIODORUS_PREWRITTEN[ln]
        elif ln in CASSIODORUS_APPENDIX:
            new_text = CASSIODORUS_APPENDIX[ln]
        elif ln == 1:
            new_text = _cassiodorus_preface()
        elif ln == 20001:
            new_text = _cassiodorus_book_header()
        else:
            new_text = _cassiodorus_generic(ln, qs, lt)

        if new_text and new_text != 'Translation pending. The original Latin text is available below.':
            cur.execute("UPDATE letters SET modern_english=? WHERE id=?", (new_text, row_id))
            fixed += 1

    conn.commit()
    return fixed


