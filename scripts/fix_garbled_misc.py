def normalize_all_caps_title(title: str) -> str:
    """Convert ALL CAPS title to Title Case, handling common abbreviations."""
    lower_words = {'to', 'the', 'of', 'and', 'a', 'an', 'in', 'on', 'at',
                   'for', 'by', 'with', 'from', 'his', 'her', 'our', 'its',
                   'or', 'nor'}
    preserve = {'I', 'II', 'III', 'IV', 'V', 'VI', 'VII', 'VIII', 'IX', 'X',
                'XI', 'XII', 'AD', 'BC'}

    words = title.split()
    result = []
    for i, w in enumerate(words):
        stripped = w.rstrip('.,;:')
        punct = w[len(stripped):]
        if stripped in preserve:
            result.append(w)
        elif i == 0:
            result.append(stripped.capitalize() + punct)
        elif stripped.upper() in {x.upper() for x in lower_words}:
            result.append(stripped.lower() + punct)
        else:
            result.append(stripped.capitalize() + punct)
    return ' '.join(result)


