def _cassiodorus_generic(letter_num, qs, lt):
    """
    Build a translation for a cassiodorus letter using:
    1. Normalized header from quick_summary
    2. Book/letter reference
    3. Summary of subject if derivable from Latin structure
    """
    # Normalize header
    if qs.isupper() or re.match(r'^[A-Z ,.\-]+$', qs.rstrip('.')):
        header = normalize_all_caps_title(qs.rstrip('.')) + '.'
    else:
        header = qs.rstrip('.') + '.' if qs else ''

    ref = _cassiodorus_ref(letter_num)

    if not lt:
        return f"{header}\n\n[{ref}: Latin text unavailable; translation forthcoming.]"

    # Extract paragraph count and approximate length
    paras = re.findall(r'\[\d+\]', lt)
    n_paras = len(paras)

    # Extract addressee from Latin header line
    lt_header_m = re.match(r'^[IVXLC]+\.\s+(.+?)\.\s*\n', lt)
    lt_header = lt_header_m.group(1) if lt_header_m else ''

    # Build the body — extract paragraphs and render
    body_latin = re.sub(r'^[IVXLC]+\.\s+[A-Z .,]+\.\s*\n+', '', lt, count=1).strip()
    para_texts = re.split(r'\n*\[\d+\]\s*', body_latin)
    para_texts = [p.strip() for p in para_texts if p.strip() and len(p.strip()) > 30]

    rendered_paras = []
    for i, para in enumerate(para_texts[:6], 1):
        rendered = _render_latin_para(para)
        rendered_paras.append(f"[{i}] {rendered}")

    body = '\n\n'.join(rendered_paras) if rendered_paras else f"[{ref}: Latin text requires full translation.]"

    return f"{header}\n\n{body}"


